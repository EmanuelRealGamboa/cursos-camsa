// Función helper para obtener artículos por categoría
export const getArticlesByCategory = (category) => {
  return blogArticles.filter(article => article.category === category);
};

// Función helper para obtener artículos destacados
export const getFeaturedArticles = () => {
  return blogArticles.filter(article => article.featured);
};

// Función helper para obtener artículo por ID
export const getArticleById = (id) => {
  return blogArticles.find(article => article.id === id);
};

// Función helper para obtener artículos más recientes
export const getRecentArticles = (limit = 4) => {
  return blogArticles
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};

export default blogArticles;
